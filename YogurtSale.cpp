void solve(){
    int n,a,b;cin>>n>>a>>b;
    int ans = 0;

    if(n%2 == 0){
        ans = min(n*a,b*(n/2));
    }
    else{
        ans = min(n*a,((n/2) * b)+a);
    }
    cout<<ans<<endl;
}
